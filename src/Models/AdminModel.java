package Models;

public class AdminModel {
    private int id;
    private String username;
    private String password;
    private String sQues;
    private String ans;

    // Getter dan Setter untuk ID
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    // Getter dan Setter untuk Username
    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    // Getter dan Setter untuk Password
    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    // Getter dan Setter untuk Security Question (sQues)
    public String getsQues() {
        return sQues;
    }

    public void setsQes(String sQues) {
        this.sQues = sQues;
    }

    // Getter dan Setter untuk Answer (ans)
    public String getAns() {
        return ans;
    }

    public void setAns(String ans) {
        this.ans = ans;
    }
}





